// Helper functions
export const getReportsByDomain = (domain) => {
  return REPORTS_DATA.filter((report) => report.domain === domain);
};

export const getReportsByFrequency = (frequency) => {
  return REPORTS_DATA.filter((report) => report.frequency === frequency);
};

export const getReportsByCompliance = (status) => {
  return REPORTS_DATA.filter((report) => report.complianceStatus === status);
};

export const getReportById = (id) => {
  return REPORTS_DATA.find((report) => report.id === id);
};

export const getTotalReports = () => REPORTS_DATA.length;

export const getReportCountByDomain = () => {
  return DOMAINS.map((domain) => ({
    domain,
    count: getReportsByDomain(domain).length,
  }));
};

export const getReportCountByFrequency = () => {
  return FREQUENCIES.map((frequency) => ({
    frequency,
    count: getReportsByFrequency(frequency).length,
  }));
};
